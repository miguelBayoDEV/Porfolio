	<h2>Cómo instalar un servidor web en tu ordenador con XAMPP.</h2>
	<p>Hablamos de convertir nuestro PC en un equipo capaz de mostrar páginas webs, y permitir que otras personas puedan acceder a ellas bien a través de Internet o, más realista, desde una red local.

	Pero su utilidad más común es crear páginas webs y poder editarlas y modificarlas de forma local, sin tener que subirlas a Internet. Así puedes perfeccionarlas antes de hacerlas públicas. También es imprescindible si vas a aprender algún lenguaje de programación relacionado con Internet: PHP, HTML, etc. para hacer prácticas.

	Instalar un servidor web suena bastante intimidante, pero es muy sencillo. Además podrás activar o desactivar el servicio simplemente pulsando un botón.
	
	XAMPP, un servidor web en pocos clics

	Existen muchos programas diferentes para instalar un servidor web en el ordenador. Hemos optado por XAMPP porque es muy sencillo de usar.

	XAMPP es un entorno de desarrollo del lenguaje PHP que incluye un servidor web. Ofrece todo lo que necesitas para crear y publicar una página web: un servidor web Apache, la base de datos MariaDB con soporte MySQL, software de desarrollo PHP, y soporte de Perl.

	Este tutorial está basado en Windows pero se hace exactamente igual en Linux u OS X, descargando la versión correspondiente.

	Vamos a comenzar descargando el software. Accede a la web de Apache Friends y descarga XAMPP for Windows. Instala el fichero que obtienes. 

	Como Windows utiliza el Control de Cuentas de Usuario (UAC) es recomendable instalarlo en una carpeta diferente a C:\Archivos de Programas, así que elige otra ubicación diferente. Por ejemplo, C:\XAMPP.</p>
