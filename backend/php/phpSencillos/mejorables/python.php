	Canales de YouTube para aprender Python desde casa
	Digital
	En Internet hay multitud de recursos para aprender algo nuevo. Y YouTube, además de vídeos graciosos y música, ofrece cursos de cualquier ámbito.
	
	En varias ocasiones he mencionado la popularidad de la programación en el mundo que vivimos. La integración de la tecnología en todos los ámbitos de nuestra vida ha hecho que saber programar sea algo necesario en muchos perfiles profesionales y que se promueva su aprendizaje en escuelas e institutos como algo natural. Incluso YouTube está repleto de vídeos al respecto.

	Uno de los lenguajes de programación de más aceptación, según los distintos índices, es Python. Entre otras cosas, porque se emplea en sectores fuera de la programación como son la investigación científica y, especialmente, el análisis de datos.

	¿Merece la pena aprender Python? Dependerá de a qué te quieras dedicar o que tengas el tiempo suficiente y la necesidad o interés por ello. Además, tienes a tu disposición multitud de recursos online para aprender este lenguaje. Incluso en YouTube hay cursos y vídeos que explican Python al detalle.
	
	Telusko

	Uno de los canales más recomendados es Telusko, un canal de programación que cuenta con tutoriales y cursos sobre Python para que te inicies en este lenguaje y aprendas todo lo necesario para considerarte todo un programador de Python.

	En concreto, su Python Tutorial for Beginners consta de más de 100 vídeos que van de 3 a 15 minutos y donde su responsable explica cualquier aspecto de Python necesario para entenderlo y aplicarlo en el día a día.

	Por lo demás, el canal está en inglés pero los vídeos cuentan con subtítulos generados automáticamente por YouTube y que debes activar manualmente.
	
	Fazt

	El canal en español Fazt ofrece vídeos divulgativos, tutoriales y otras curiosidades relacionadas con Python, programación Web, bases de datos, Linux o desarrollo de aplicaciones móviles.

	Aunque hay varios que tratan sobre Python, destacan los de Machine Learning y Curso de Python basado en la versión 3 de Python.

	Como ocurre con cualquier contenido de YouTube, puedes consumirlo en cualquier dispositivo, verlo seguido o por partes y volverlos a ver si es necesario mientras practicas en tu ordenador.</p>
