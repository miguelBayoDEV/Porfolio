import java.lang.*;
public class Ejercicio35 {
	public static void main(String[] args){
		String n1 = "125";
		String n2 = "156";
		int número1 = Integer.parseInt(n1);
		int número2 = Integer.parseInt(n2);
		int suma = número1 + número2;
		int producto = número1 * número2;
		System.out.println("La suma de 125 y 156 es: "+suma+".");
		System.out.println("El producto de 125 y 156 es: "+producto+".");
	}
}